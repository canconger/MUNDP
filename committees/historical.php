      <br><br>
      Britain’s expansionist policy has led to the failed Jameson Raid of 1895 where Britain had hoped to cause the rebellion of the uitlanders against the Boers. Tensions escalated in the area for the control of precious mining industries in the area and the British will to incorporate the Transvaal and the Orange Free State into a federation under the British control. In June 1899 negotiations in Bloemfontein to solve the dispute diplomatically failed and the British Colonial Secretary Joseph Chamberlain demanded full voting rights and representation for the uitlanders. Knowing that giving into the demands of the British would result in the Boers losing their power in the area, the Boers rejected this demand and issued an ultimatum on October 1899 to withdraw all British forces in the border regions of Transvaal and Orange Free State. The British government rejected the ultimatum and incentivated by the lack of British troops in the area Orange Free State and Transvaal declared war to the British causing the Second Boer War.
